public class Arthmetic{
    public static void main(String[] args){
        int i = 25;
        double d = 11.1;
        //addition 
        System.out.println("Addition: "+ (i+d));
        //Subtraction
        System.out.println("Subtraction: "+ (i-d));
        //Multiplication
        System.out.println("Multiplication: "+ (i*d));
        //Dividation
        System.out.println("Dividation: "+ (i/d));
        //Remainder or Modulus Operation
        System.out.println("Remainder or Modulus Operation: "+ (i%d));
    }
}